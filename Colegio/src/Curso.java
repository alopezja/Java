public class Curso {
    
    private int id;
    private String nombre;
    
    public Curso(){}
    public Curso(String nombre){
    
        this.nombre = nombre;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
}
